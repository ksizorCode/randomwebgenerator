        <p>Sombragrís (Shadowfax en el original inglés) es un caballo ficticio del legendarium de J. R. R. Tolkien, que aparece en la novela El Señor de los Anillos. Sombragrís es uno de los mearas, los señores de los caballos, que eran domados y montados exclusivamente por los reyes de Rohan. Cabe destacar su enorme inteligencia, lealtad y, ante todo, velocidad y resistencia. Es de color gris plateado y puede entender la lengua de los hombres.

Sombragrís fue prestado por el rey Théoden a Gandalf, aunque después de que el mago liberara al rey del embrujo de Saruman, este se lo ofreció como regalo en gesto de gratitud y ya no se separó del caballo. Sombragrís también dejó montar en algún momento a Pippin y a Gimli el Enano, aunque ambas veces Gandalf iba montado también. Tras la Guerra del Anillo, Sombragrís fue con Gandalf a Aman.</p>

